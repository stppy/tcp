package py.gov.stp.objetosV2;

public class Ws {
	protected int id;
	protected String nombre;
	protected String descripcion;
	protected String url;
	protected String metodo;
	protected String usuario;
	protected String pass;
	protected String idClave;
	protected String idValor;
	protected int wsTipoId;
	protected boolean borrado;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getMetodo() {
		return metodo;
	}
	public void setMetodo(String metodo) {
		this.metodo = metodo;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getIdClave() {
		return idClave;
	}
	public void setIdClave(String idClave) {
		this.idClave = idClave;
	}
	public String getIdValor() {
		return idValor;
	}
	public void setIdValor(String idValor) {
		this.idValor = idValor;
	}
	public int getWsTipoId() {
		return wsTipoId;
	}
	public void setWsTipoId(int wsTipoId) {
		this.wsTipoId = wsTipoId;
	}
	public boolean isBorrado() {
		return borrado;
	}
	public void setBorrado(boolean borrado) {
		this.borrado = borrado;
	}
	
	

}
