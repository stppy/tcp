package py.gov.stp.objetosV2;

public class UnidadResponsable {
protected int id;
protected String nombre;
protected String descripcion;
protected String abrev;
protected int numeroFila;
protected int entidadId;
protected int nivelId;
protected int unidadJerarquica;
protected String anho;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public String getDescripcion() {
	return descripcion;
}
public void setDescripcion(String descripcion) {
	this.descripcion = descripcion;
}
public String getAbrev() {
	return abrev;
}
public void setAbrev(String abrev) {
	this.abrev = abrev;
}
public int getNumeroFila() {
	return numeroFila;
}
public void setNumeroFila(int numeroFila) {
	this.numeroFila = numeroFila;
}
public int getEntidadId() {
	return entidadId;
}
public void setEntidadId(int entidadId) {
	this.entidadId = entidadId;
}
public int getNivelId() {
	return nivelId;
}
public void setNivelId(int nivelId) {
	this.nivelId = nivelId;
}
public int getUnidadJerarquica() {
	return unidadJerarquica;
}
public void setUnidadJerarquica(int unidadJerarquica) {
	this.unidadJerarquica = unidadJerarquica;
}
public String getAnho() {
	return anho;
}
public void setAnho(String anho) {
	this.anho = anho;
}


}
