public class GrupoRol {
	protected int grupo_id;
	protected int role_id;
	protected int modulo_id;
	protected int registro_id;
	public int getGrupo_id() {
		return grupo_id;
	}
	public void setGrupo_id(int grupo_id) {
		this.grupo_id = grupo_id;
	}
	public int getRole_id() {
		return role_id;
	}
	public void setRole_id(int role_id) {
		this.role_id = role_id;
	}
	public int getModulo_id() {
		return modulo_id;
	}
	public void setModulo_id(int modulo_id) {
		this.modulo_id = modulo_id;
	}
	public int getRegistro_id() {
		return registro_id;
	}
	public void setRegistro_id(int registro_id) {
		this.registro_id = registro_id;
	}
	
	

}
