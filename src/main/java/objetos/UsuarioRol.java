package objetos;

public class UsuarioRol {
	protected int usuario_id;
	protected int rol_id;
	protected int modulo_id;
	protected int registro_id;
	
	public int getUsuario_id() {
		return usuario_id;
	}
	public void setUsuario_id(int usuario_id) {
		this.usuario_id = usuario_id;
	}
	public int getRol_id() {
		return rol_id;
	}
	public void setRol_id(int rol_id) {
		this.rol_id = rol_id;
	}
	public int getModulo_id() {
		return modulo_id;
	}
	public void setModulo_id(int modulo_id) {
		this.modulo_id = modulo_id;
	}
	public int getRegistro_id() {
		return registro_id;
	}
	public void setRegistro_id(int registro_id) {
		this.registro_id = registro_id;
	}
	
	

}
